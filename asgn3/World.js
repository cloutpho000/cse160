function drawMap() {
    const wall = new Cube();
    wall.color = [1, 1, 1, 1];
    wall.textureNum = 2;
    for (let x = 0; x < g_map.length; x++) {
      for (let y = 0; y < g_map[0].length; y++) {
        let height = g_map[x][y];
        if (height > 0) {
          for (let h = 0; h < height; h++) {
            wall.matrix.setIdentity();
            wall.matrix.translate(x-16, h - 1, y - 16);
            wall.render();
          }
        }
      }
    }
  }
  

class Vector {
    constructor(x, y, z){
        this.x = x;
        this.y = y;
        this.z = z;
    }
    add(v){ return new Vector(this.x + v.x, this.y + v.y, this.z + v.z); }
    subtract(v){ return new Vector(this.x - v.x, this.y - v.y, this.z - v.z); }
    multiply(scalar){ return new Vector(this.x * scalar, this.y * scalar, this.z * scalar); }
    divide(scalar){ return new Vector(this.x / scalar, this.y / scalar, this.z / scalar); }
    length(){ return Math.sqrt(this.x**2 + this.y**2 + this.z**2); }
    normalize(){ return this.divide(this.length()); }
    cross(v){ return new Vector(
        this.y * v.z - this.z * v.y,
        this.z * v.x - this.x * v.z,
        this.x * v.y - this.y * v.x
    ); }
}




class Camera{
    constructor(){
        this.eye = new Vector(0,0,3);
        this.at = new Vector (0,0, -100);
        this.up = new Vector(0, 1, 0);
    }
    panLeft(alpha = 2){
        let f = this.at.subtract(this.eye);
        let rotationMatrix = new Matrix4();
        rotationMatrix.setRotate(alpha, this.up.x, this.up.y, this.up.z);
        let f_vec3 = new Vector3([f.x, f.y, f.z]);
        let f_prime_vec3 = rotationMatrix.multiplyVector3(f_vec3);
        let f_prime = new Vector(
            f_prime_vec3.elements[0],
            f_prime_vec3.elements[1],
            f_prime_vec3.elements[2]
        );
        this.at = this.eye.add(f_prime);
    }
    panRight(alpha = 2){
        let f = this.at.subtract(this.eye);
        let rotationMatrix = new Matrix4();
        rotationMatrix.setRotate(-alpha, this.up.x, this.up.y, this.up.z);
        let f_vec3 = new Vector3([f.x, f.y, f.z]);
        let f_prime_vec3 = rotationMatrix.multiplyVector3(f_vec3);

        let f_prime = new Vector(
            f_prime_vec3.elements[0],
            f_prime_vec3.elements[1],
            f_prime_vec3.elements[2]
        );
        this.at = this.eye.add(f_prime);
    }
    forward(step = .2) {
        var f = this.at.subtract(this.eye);
        f = f.normalize().multiply(step);
        this.at = this.at.add(f);
        this.eye = this.eye.add(f);
        console.log("forward activated!");
    }
    back(step = .2){
        var f = this.at.subtract(this.eye);
        f = f.normalize().multiply(step);
        this.at = this.at.subtract(f);
        this.eye = this.eye.subtract(f);
        console.log("back activated!")
    }
    left(step = .2){
        var f = this.eye.subtract(this.at);
        f = f.normalize().multiply(step);
        var s = f.cross(this.up);
        s = s.normalize();
        this.at = this.at.add(s);
        this.eye = this.eye.add(s);
    }
    right(step = .2) {
        var f = this.at.subtract(this.eye);
        f = f.normalize().multiply(step);
        var s = f.cross(this.up);
        s = s.normalize();
        this.eye = this.eye.add(s);
        this.at = this.at.add(s);
    }
    getViewMatrix(){
        let view = new Matrix4();
        view.setLookAt(
            this.eye.x, this.eye.y, this.eye.z,
            this.at.x, this.at.y, this.at.z,
            this.up.x, this.up.y, this.up.z
        );
        return view;
    }
}
